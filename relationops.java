class relationops{
    public static void main(String args[]){
        int x = 3;
        int y = 8;
        double h = 5.6;
        double m = 6.5;
        double g = 8.0;
        // relationops
        boolean res = x<y;System.out.println(res);
        boolean res1 = x>y;System.out.println(res1);
        boolean res2 = x>=y;System.out.println(res2);
        boolean res3 = x<=y;System.out.println(res3);
        boolean res4 = h!=m;System.out.println(res4);
        boolean res5 = g==y;System.out.println(res5);

    }
}